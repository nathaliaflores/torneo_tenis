
package modelo;


public class Fase {
    private int idFase;
    private String nombre;

    public Fase() {
    }

    public Fase(int idFase) {
        this.idFase = idFase;
    }

    public int getIdFase() {
        return idFase;
    }

    public void setIdFase(int idFase) {
        this.idFase = idFase;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
